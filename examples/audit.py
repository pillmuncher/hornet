from hornet import database
from hornet.modalities import modal
from hornet.symbols import (
    T1,
    T2,
    TX,
    Action,
    Agent,
    Amount,
    D,
    Fact,
    Limit,
    R,
    Rpt,
    Rule,
    T,
    _,
    before,
    came_into_force,
    combine,
    compliance_world,
    epistemic_world,
    holds_role,
    learned,
    let,
    necessarily,
    obligation,
    performed,
    report_contains,
    report_generated,
    review_report,
    reviewed,
    smaller,
    threshold,
    transaction,
    treated_as_known,
    uncertain_fact,
    violation,
)


def main() -> None:
    db = modal(database())

    db.tell(
        #
        # ── Domain events ────────────────────────────────────────────────
        #
        transaction('tx17', 'employee_e', 250_000, 1),
        report_generated('rep42', 'tx17', 2),
        #
        # ── Normative acts ───────────────────────────────────────────────
        #
        came_into_force('r1', 0),
        threshold('r1', 0, 100_000),
        holds_role('alice', 'cfo', 0),
        #
        # ── Temporal rules ─────────────────────────────────────────────────
        #
        before(T1, T2).when(smaller(T1, T2)),
        #
        # ── Domain rules ─────────────────────────────────────────────────
        #
        violation(TX, Rule).when(
            transaction(TX, _, Amount, T),
            threshold(Rule, T1, Limit),
            ~before(T, T1),
            let(D, Amount - Limit),
            smaller(0, D),
        ),
        report_contains(R, violation(TX, Rule)).when(
            report_generated(R, TX, T),
            violation(TX, Rule),
        ),
        learned(Agent, Fact, T).when(
            reviewed(Agent, R, T),
            report_contains(R, Fact),
        ),
        reviewed(Agent, Rpt, T).when(
            performed(Agent, review_report(Rpt), T),
        ),
        obligation(Agent, review_report(Rpt), T).when(
            holds_role(Agent, 'cfo', T1),
            ~before(T, T1),
            report_generated(Rpt, _, T2),
            ~before(T, T2),
        ),
        # What counts as epistemically uncertain for an agent at time T:
        # any report generated by T that the agent hasn't reviewed.
        uncertain_fact(Agent, report_contains(Rpt, Fact), T).when(
            report_generated(Rpt, _, T1),
            ~before(T, T1),
            report_contains(Rpt, Fact),
            ~performed(Agent, review_report(Rpt), T),
        ),
        # treated_as_known: necessarily, in all compliance worlds,
        # necessarily, in all epistemic worlds, the agent learned the fact.
        treated_as_known(Agent, Fact, T).when(
            necessarily(
                learned(Agent, Fact, T),
                combine(
                    epistemic_world(uncertain_fact(Agent, Fact, T), Agent, T),
                    compliance_world(obligation(Agent, Action, T), Agent, T),
                ),
            )
        ),
    )

    # ── Query ────────────────────────────────────────────────────────
    t_query = 2
    result = any(db.ask(treated_as_known('alice', violation('tx17', 'r1'), t_query)))
    print(f'treated_as_known(alice, violation(tx17, r1), {t_query}): {result}')


if __name__ == '__main__':
    main()
